export function zh(value) {
  const text = String(value ?? '').trim()
  if (!text) return ''
  const translated = ZH[text]
  if (translated) return translated
  if (/^Active context for\s+.+\.$/i.test(text)) {
    return `当前活动上下文：${zh(text.replace(/^Active context for\s+/i, '').replace(/\.$/, ''))}。`
  }
  if (/^State changed to\s+/i.test(text)) {
    return `状态已变更为 ${zh(text.replace(/^State changed to\s+/i, ''))}`
  }
  if (/^Permission mode changed to\s+/i.test(text)) {
    return `许可模式已切换为 ${zh(text.replace(/^Permission mode changed to\s+/i, ''))}`
  }
  if (/^Tool called:\s*/i.test(text)) {
    return `工具已调用：${text.replace(/^Tool called:\s*/i, '')}`
  }
  if (/^(Planner|Builder|Researcher|Reviewer|Operator):\s*/i.test(text)) {
    const [, role, detail] = text.match(/^(Planner|Builder|Researcher|Reviewer|Operator):\s*(.*)$/i) || []
    return `${zh(role)}：${zh(detail)}`
  }
  if (/^Review check failed:\s*/i.test(text)) {
    return `审查检查失败：${zh(text.replace(/^Review check failed:\s*/i, ''))}`
  }
  if (/^Review check passed:\s*/i.test(text)) {
    return `审查检查通过：${zh(text.replace(/^Review check passed:\s*/i, ''))}`
  }
  if (/^Review recovery action opened$/i.test(text)) {
    return '审查恢复动作已打开'
  }
  if (/^Review recovery action resolved$/i.test(text)) {
    return '审查恢复动作已解决'
  }
  if (/^Review requested from Artifacts workspace for\s+/i.test(text)) {
    return `已从产物工作区请求审查：${zh(text.replace(/^Review requested from Artifacts workspace for\s+/i, '').replace(/\.$/, ''))}。`
  }
  if (/^Review requested from Agents surface for\s+/i.test(text)) {
    return `已从智能体工作面请求审查：${zh(text.replace(/^Review requested from Agents surface for\s+/i, '').replace(/\.$/, ''))}。`
  }
  if (/^Repair review check:\s*/i.test(text)) {
    return `修复审查检查：${zh(text.replace(/^Repair review check:\s*/i, ''))}`
  }
  if (/^Repair requested:\s*/i.test(text)) {
    return `已请求修正：${zh(text.replace(/^Repair requested:\s*/i, ''))}`
  }
  if (/^Complete blocked:\s*repair review check\s*/i.test(text)) {
    const match = text.match(/^Complete blocked:\s*repair review check\s+"([^"]+)"\s+in the Review or Guard Spine\.$/i)
    if (match) return `完成被阻止：请修复审查检查 "${zh(match[1])}"，位置在审查或守护脊柱。`
    return text
      .replace(/^Complete blocked:\s*repair review check\s*/i, '完成被阻止：请修复审查检查 ')
      .replace(/\s+in the Review or Guard Spine\.$/i, '，位置在审查或守护脊柱。')
  }
  if (/^Complete blocked:\s*/i.test(text)) {
    return text.replace(/^Complete blocked:\s*/i, '完成被阻止：')
  }
  if (/^Review\s+/i.test(text)) {
    return `审查 ${zh(text.replace(/^Review\s+/i, ''))}`
  }
  if (/^Resolved blocking review check:\s*/i.test(text)) {
    return text.replace(/^Resolved blocking review check:\s*/i, '已解决阻塞审查检查：')
  }
  if (/^Approved:\s*/i.test(text)) {
    return text
      .replace(/^Approved:\s*/i, '已批准：')
      .replace(/\. Resuming mission\.$/i, '。任务继续运行。')
  }
  if (/^Denied:\s*/i.test(text)) {
    return text
      .replace(/^Denied:\s*/i, '已拒绝：')
      .replace(/\. Mission needs an alternative\.$/i, '。任务需要替代方案。')
  }
  if (/^Permission denied:\s*/i.test(text)) {
    return `许可已拒绝：${zh(text.replace(/^Permission denied:\s*/i, ''))}`
  }
  if (/^Permission approved:\s*/i.test(text)) {
    return `许可已批准：${zh(text.replace(/^Permission approved:\s*/i, ''))}`
  }
  if (/^Plan policy blocked\s+/i.test(text)) {
    return `计划策略已阻断：${zh(text.replace(/^Plan policy blocked\s+/i, '').replace(/\.$/, ''))}`
  }
  if (/^Approved from Guard Spine:\s*/i.test(text)) {
    return `来自守护脊柱的批准：${zh(text.replace(/^Approved from Guard Spine:\s*/i, ''))}`
  }
  if (/^Denied from Guard Spine:\s*/i.test(text)) {
    return `来自守护脊柱的拒绝：${zh(text.replace(/^Denied from Guard Spine:\s*/i, ''))}`
  }
  return text
}

export function zhOnly(value) {
  const text = String(value ?? '').trim()
  return ZH[text] || text
}

export function zhPrefix(prefix, value) {
  const translatedPrefix = zhOnly(prefix)
  const text = String(value ?? '').trim()
  return `${translatedPrefix} ${text}`
}
